const seedDB = async () => {
    try {
        await mongoose.connect(process.env.MONGO_URI);
        console.log('MongoDB Connected for Seeding');

        // Prepare State data with EXTRA info
        const statesToInsert = STATES_STATIC.map(state => {
            const extra = STATE_EXTRA[state._id] || {};
            return {
                ...state,
                ...extra
            };
        });

        console.log('Clearing old data...');
        await State.deleteMany();
        await Package.deleteMany();
        await Hotel.deleteMany();

        console.log('Inserting states...');
        await State.insertMany(statesToInsert);

        console.log('Inserting packages...');
        await Package.insertMany(HOLIDAY_PACKAGES);

        console.log('Inserting hotels...');
        await Hotel.insertMany(HOTELS_DATA);

        console.log('Database Seeding Completed Successfully!');
        process.exit();
    } catch (err) {
        console.error('Error seeding DB:', err);
        process.exit(1);
    }
};

seedDB();
